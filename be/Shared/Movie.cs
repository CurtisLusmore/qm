namespace be.Shared;

public record Movie(
    string Id,
    TitleType Type,
    string Name,
    int? Year,
    string ImageUrl,
    int? EndYear,
    DateTime? ReleaseDate,
    string Plot,
    string[] Genres,
    string? TrailerUrl,
    string Classification,
    Ratings Ratings,
    PersonSummary[] Cast,
    PersonSummary[] Directors,
    PersonSummary[] Writers,
    bool Downloaded = false,
    MediaFile? MediaFile = null)
: Title(
    Id,
    Type,
    Name,
    Year,
    ImageUrl,
    EndYear,
    ReleaseDate,
    Plot,
    Genres,
    TrailerUrl,
    Classification,
    Ratings,
    Cast,
    Directors,
    Writers,
    Downloaded);